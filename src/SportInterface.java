
/**
 * Interface for all of the classes
 * @author anuccio
 *
 */
public interface SportInterface {
	/**
	 * gets the home team
	 * @return returns the home team
	 */
	public String getHomeTeam();
	/**
	 * gets the away team
	 * @return the away team
	 */
	public String getAwayTeam();
	/**
	 * gets the periodname
	 * @return Name of period
	 */
	public String getPeriodName();
	/**
	 * gets the home score
	 * @return Home score
	 */
	public int getHomeScore();
	/**
	 * gets the away score
	 * @return Away Score
	 */
	public int getAwayScore();
	/**
	 * Sets the home score
	 * @param h home score
	 */
	public void setHomeScore(int h);
	/**
	 * sets the away score
	 * @param a away score
	 */
	public void setAwayScore(int a);
	/**
	 * sets the period
	 */
	public void setperiod();
	/**
	 * gets the period
	 * @return period name
	 */
	public int getperiod();
	/**
	 * gets the gamestatus
	 * @return returns if its over or not
	 */
	public boolean GameStatus();
	/**
	 * gets the winner
	 * @return the winning team
	 */
	public String getWinner();
}
